package Game.Cars;

public class ThreeWheeler extends Car {

	private static final String NAME = "Three Wheeler";
	private static final int COST = 500;
	private static final int SPEEDLIMIT = 63;
	private static final int HP = 39;
	private static final int SEATS = 5;
	
	public ThreeWheeler() {
		super();
		this.name = ThreeWheeler.NAME;
		this.cost = ThreeWheeler.COST;
		this.speedLimit = ThreeWheeler.SPEEDLIMIT;
		this.hp = ThreeWheeler.HP;
		this.seats = ThreeWheeler.SEATS;
		
	}
	
}
